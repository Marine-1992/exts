def add(number_one, number_two):
    result = number_one + number_two
    print(result)

add(5,3)
add(11.2, 8.8)
add(10, -10)

def subtract(number_one, number_two):
    result = number_one - number_two
    print(result)

subtract(5,3)
subtract(11.2, 8.8)
subtract(10, -10)

def multiply(number_one, number_two, number_three):
    result = number_one * number_two * number_three
    print(result)

multiply(1, 2, 3)
multiply(2, 3, 4)
multiply(3, 4, 5)

